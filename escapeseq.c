#include <stdio.h>

int main() {
    printf("1. Newline (\\n):\n");
    printf("Hello\nWorld\n");

    printf("\n2. Tab (\\t):\n");
    printf("Hello\tWorld\n");

    printf("\n3. Backslash (\\\\):\n");
    printf("This is a backslash: \\\n");

    printf("\n4. Single Quote (\\'):\n");
    printf("It\'s a sunny day\n");

    printf("\n5. Double Quote (\\\"):\n");
    printf("She said, \"Hello!\"\n");

    printf("\n6. Carriage Return (\\r):\n");
    printf("12345\rAB\n"); 
    printf("\n7. Backspace (\\b):\n");
    printf("Hello\b World\n");  
    printf("\n8. Form Feed (\\f):\n");
    printf("Hello\fWorld\n"); 

    printf("\n9. Bell (\\a):\n");
    printf("Alert\a\n");  
    return 0;
}